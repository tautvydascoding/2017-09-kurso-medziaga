package com.company;

public class Duomenys {

    public double suma(double sk1, double sk2) {
        return sk1 + sk2;
    }

    public double atimtis(double sk1, double sk2) {
        return sk1 - sk2;
    }

    public double sandauga(double sk1, double sk2) {
        return sk1 * sk2;
    }

    public double dalyba(double sk1, double sk2) {
        return sk1 / sk2;
    }
}
